#step1:建表（qt_dim_hour）
CREATE TABLE IF NOT EXISTS qt_smartreport.qt_dim_hour
(
    `id`                   int(20)      NOT NULL AUTO_INCREMENT COMMENT '主键',
    `hour_start_time`      varchar(255) NOT NULL COMMENT '小时开始时间',
    `next_hour_start_time` varchar(255) NOT NULL COMMENT '下一个小时开始时间',
    `created_time`         timestamp    NOT NULL DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
    `updated_time`         timestamp    NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
    PRIMARY KEY (`id`),
    key idx_hour_start_time (`hour_start_time`),
    key idx_next_hour_start_time (`next_hour_start_time`)

)
    ENGINE = InnoDB
    DEFAULT CHARACTER SET = utf8
    COLLATE = utf8_general_ci
    AUTO_INCREMENT = 1
    ROW_FORMAT = DYNAMIC COMMENT ='小时维表';
	
	
	

